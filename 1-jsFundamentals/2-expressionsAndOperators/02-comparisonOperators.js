console.log("3" == 3);
// TRUE, coersion, == does not check for data type, only value

console.log("3" === 3);
// FALSE, strict equality check does check for data type

console.log("3" != 3);
//not equal

console.log("3" !== 3);
//strict not equal

console.log(3 > 1);
//greater than

console.log(3 < 1);
//less than

console.log(5 >= 3);
//greater equal than

console.log(5 <= 4);
//less than or equal to

console.log(1 > 0 && 2 > 1);
//and

console.log(1 > 0 || 2 > 7);
//or (will return true if ONE side is true)
