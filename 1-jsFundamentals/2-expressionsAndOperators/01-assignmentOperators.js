//assignment operator (=, +, - sign)
let r = 5;

//addition assignment
r = r + 1;
//or
r += 1; //shorthand

//subtraction assignment
r = r - 1;
//or
r -= 1;

//multiplication assignment
r = r * 1;
r *= 1;

//divisoin assignment
r = r / 1;
r /= 1;

// returns the remainder of the division problem (modulus)
r = r % 1;

//exponential assignment
r = r ** 1;
r **= 1;